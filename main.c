#include "heads/for_main.h"

int main() {
    //显示主菜单
    main_menu();

    //读取用户输入，判断执行方向
    char main_menu_input;
    re_input_main_menu_choice:
    printf("-> 请输入选项（1/2/3）");
    fflush(stdin);
    scanf("%c",&main_menu_input);
    switch (main_menu_input) {
        //登录
        case '1':{
            login_module();
            break;
        }
        //注册
        case '2':{
            registered_module();
            break;
        }
        //退出
        case '3':{
            exit(0);
        }
        //输入不合法
        default:{
            printf("->输入不合法，请重新输入！\n\n");
            fflush(stdin);
            goto re_input_main_menu_choice;
        }
    }
    return 0;
}
